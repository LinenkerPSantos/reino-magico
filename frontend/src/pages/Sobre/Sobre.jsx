function Blocks({ blocks }) {
  return blocks.map((b, i) => {
    if (b.p) return <p key={i}>{b.p}</p>
    if (b.contact) return <p key={i} className={styles.contact}>{b.contact}</p>
    if (b.list) return (
      <ul key={i} className={styles.list}>
        {b.list.map((item, j) => <li key={j}>{item}</li>)}
      </ul>
    )
    return null
  })
}

